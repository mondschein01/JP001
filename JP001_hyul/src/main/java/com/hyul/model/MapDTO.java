package com.hyul.model;

public class MapDTO {
	private String category;
	private String hyulName;
	private String hyulPos;
	private String hyulSelect;
	private String wayToChim;
	private String hyulEffect;
	private String imgPath;

	public MapDTO() {
		super();
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getHyulName() {
		return hyulName;
	}

	public void setHyulName(String hyulName) {
		this.hyulName = hyulName;
	}

	public String getHyulPos() {
		return hyulPos;
	}

	public void setHyulPos(String hyulPos) {
		this.hyulPos = hyulPos;
	}

	public String getHyulSelect() {
		return hyulSelect;
	}

	public void setHyulSelect(String hyulSelect) {
		this.hyulSelect = hyulSelect;
	}

	public String getWayToChim() {
		return wayToChim;
	}

	public void setWayToChim(String wayToChim) {
		this.wayToChim = wayToChim;
	}

	public String getHyulEffect() {
		return hyulEffect;
	}

	public void setHyulEffect(String hyulEffect) {
		this.hyulEffect = hyulEffect;
	}

	public String getImgPath() {
		return imgPath;
	}

	public void setImgPath(String imgPath) {
		this.imgPath = imgPath;
	}
}
